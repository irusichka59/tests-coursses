# 2 Створіть функцію, яка може друкувати квадрати парних чисел у діапазоні від 0 до 1000000000 включно.
# Рішення має працювати і не фрізити комп’ютер.

# #Генератори дозволяють отримувати значення по одному, що дозволяє ефективно працювати з великими обсягами даних без
# # перевантаження пам'яті.
def generate_even_squares():
    for i in range(0, 1000000001, 2):
        yield i ** 2


# Виведення квадратів парних чисел у консоль
for square in generate_even_squares():
    print(square)
